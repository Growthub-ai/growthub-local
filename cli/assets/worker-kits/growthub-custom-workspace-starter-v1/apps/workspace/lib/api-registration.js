/**
 * API Registration engine — turns operator input into a governed paired plan:
 * an api-registry CONFIG row + an optional RESOLVER FILE proposal + env/auth
 * requirement + a test plan + readiness. Pure (no fetch / no fs). The Register
 * API wizard and the helper register_api lane build the SAME plan and apply it
 * through POST /api/workspace/register-api.
 *
 * Contract: the row carries only metadata + the authRef slug — never a secret.
 * The generated resolver never inlines a secret.
 */

const API_REGISTRY_COLUMNS = [
  "integrationId", "authRef", "baseUrl", "endpoint", "method", "status",
  "lastTested", "lastResponse", "entityTypes", "description", "connectorKind",
  "resolverTemplateId", "schemaVersion", "capabilities", "executionLane",
];

const HTTP_METHODS = ["GET", "POST", "PUT", "PATCH", "DELETE"];
const CONFIG_ONLY_TEMPLATES = new Set(["custom-http", "webhook", "nango"]);
const SUCCESS_STATUSES = new Set(["connected", "approved", "ok", "success"]);

function clean(v) { return String(v ?? "").trim(); }
function slugifyId(v) {
  return clean(v).toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "").slice(0, 64);
}
function normalizeMethod(v) {
  const m = clean(v).toUpperCase();
  return HTTP_METHODS.includes(m) ? m : "GET";
}

/** A resolver is required when the response must be shaped into rows. */
function resolverRequired(input) {
  if (input?.needsResolver === true) return true;
  if (clean(input?.connectorKind).toLowerCase() === "custom") return true;
  const t = clean(input?.resolverTemplateId).toLowerCase();
  if (t && !CONFIG_ONLY_TEMPLATES.has(t)) return true;
  return Boolean(clean(input?.recordsPath) || clean(input?.entityType));
}

function resolverFileName(integrationId) {
  const slug = slugifyId(integrationId);
  return slug ? `${slug}.js` : "";
}
function resolverPath(integrationId) {
  const f = resolverFileName(integrationId);
  return f ? `lib/adapters/integrations/resolvers/${f}` : "";
}

function envCandidateNames(authRef) {
  const t = clean(authRef).replace(/[^a-z0-9]+/gi, "_").replace(/^_+|_+$/g, "").toUpperCase();
  return t ? [t, `${t}_API_KEY`, `${t}_TOKEN`] : [];
}
function isResolved(authRef, env) {
  const src = env && typeof env === "object" ? env : {};
  return envCandidateNames(authRef).some((k) => Boolean(src[k]));
}

/** Validate + normalize input into an api-registry row. Returns { row, errors }. */
function normalizeApiRegistryInput(input = {}) {
  const errors = [];
  const integrationId = slugifyId(input.integrationId || input.name);
  if (!integrationId) errors.push("integrationId (or name) is required");
  const baseUrl = clean(input.baseUrl);
  const endpoint = clean(input.endpoint);
  if (!baseUrl && !endpoint) errors.push("baseUrl or endpoint is required");
  if (baseUrl && !/^https?:\/\//i.test(baseUrl)) errors.push("baseUrl must be an absolute http(s) URL");
  if (clean(input.secret) || clean(input.secretValue) || clean(input.apiKey)) {
    errors.push("secret values must not be sent to API registration — set them in Settings (.env.local)");
  }
  const row = {
    integrationId,
    authRef: slugifyId(input.authRef),
    baseUrl,
    endpoint,
    method: normalizeMethod(input.method),
    status: "untested",
    lastTested: "",
    lastResponse: "",
    entityTypes: clean(input.entityType || input.entityTypes),
    description: clean(input.description),
    connectorKind: clean(input.connectorKind) || "http",
    resolverTemplateId: clean(input.resolverTemplateId) || "custom-http",
    schemaVersion: "growthub-api-registry-v1",
    capabilities: clean(input.capabilities) || "fetchRecords",
    executionLane: clean(input.executionLane) || "data-source",
  };
  return { row, errors };
}

/** Generate a loader-valid resolver module (registerSourceResolver contract). */
function buildResolverSource(input = {}) {
  const integrationId = slugifyId(input.integrationId || input.name);
  const entityType = clean(input.entityType) || `${integrationId}.records`;
  const recordsPath = clean(input.recordsPath);
  const method = normalizeMethod(input.method);
  const pathComment = recordsPath
    ? `    // Records read from response.${recordsPath}`
    : "    // Records read from a top-level array, or .data / .results / .items";
  const pickRecords = recordsPath
    ? recordsPath.split(".").reduce((acc, key) => `${acc}?.[${JSON.stringify(key)}]`, "payload")
    : "(Array.isArray(payload) ? payload : payload?.data ?? payload?.results ?? payload?.items ?? [])";
  return `import { registerSourceResolver } from "../source-resolver-registry.js";

// Auto-generated by the Register API wizard for "${integrationId}".
// The API Registry row owns endpoint/auth metadata; this file owns the
// response normalization the generic HTTP path cannot infer.
// SECURITY: never inline a secret. The runtime injects the auth value resolved
// from the authRef env slug at call time (config.authValue).

registerSourceResolver({
  integrationId: ${JSON.stringify(integrationId)},
  entityTypes: [${JSON.stringify(entityType)}],
  connectorKind: "http",

  listEntities: async () => [{ id: ${JSON.stringify(entityType)}, label: ${JSON.stringify(entityType)}, type: "collection" }],

  fetchRecords: async (config) => {
    const baseUrl = String(config?.baseUrl || "").replace(/\\/+$/, "");
    const endpoint = String(config?.endpoint || "");
    const url = /^https?:\\/\\//i.test(endpoint) ? endpoint : baseUrl + "/" + endpoint.replace(/^\\/+/, "");
    const headers = { accept: "application/json" };
    if (config?.authValue) {
      const headerName = config.authHeaderName || "authorization";
      const prefix = config.authPrefix || "";
      headers[headerName] = prefix ? prefix + " " + config.authValue : config.authValue;
    }
    const res = await fetch(url, { method: ${JSON.stringify(method)}, headers });
    const payload = await res.json().catch(() => ([]));
${pathComment}
    const records = ${pickRecords};
    return Array.isArray(records) ? records : [];
  },
});
`;
}

function findApiRegistryObject(config) {
  const objects = Array.isArray(config?.dataModel?.objects) ? config.dataModel.objects : [];
  return objects.find((o) => o?.objectType === "api-registry") || null;
}

function buildTestUrl(row) {
  const baseUrl = clean(row.baseUrl).replace(/\/+$/, "");
  const endpoint = clean(row.endpoint);
  if (/^https?:\/\//i.test(endpoint)) return endpoint;
  if (!baseUrl) return "";
  return endpoint ? `${baseUrl}/${endpoint.replace(/^\/+/, "")}` : baseUrl;
}

/** Build the full paired registration plan. */
function buildApiRegistrationPlan(input = {}, { existingConfig = {}, env = {} } = {}) {
  const { row, errors } = normalizeApiRegistryInput(input);
  const apiObject = findApiRegistryObject(existingConfig);
  const collision = apiObject
    ? (apiObject.rows || []).some((r) => clean(r?.integrationId) === row.integrationId)
    : false;
  if (collision) errors.push(`integrationId "${row.integrationId}" already exists in the API Registry`);

  const needsResolver = resolverRequired(input);
  const resolver = needsResolver
    ? {
        required: true,
        path: resolverPath(row.integrationId),
        filename: resolverFileName(row.integrationId),
        integrationId: row.integrationId,
        source: buildResolverSource({ ...input, integrationId: row.integrationId }),
        linkage: { registryField: "integrationId", value: row.integrationId },
      }
    : { required: false };

  const authConfigured = row.authRef ? isResolved(row.authRef, env) : false;
  const testUrl = buildTestUrl(row);

  // Optional paired Data Source row — projects this API's records into the
  // workspace via the source-records sidecar (sourceStorage), reusing the
  // existing refresh-sources flow. References the api-registry row by registryId.
  const wantsDataSource = input?.createDataSource === true || clean(input?.outputMode).toLowerCase() === "data-source";
  const dataSource = wantsDataSource
    ? {
        create: true,
        row: {
          Name: clean(input.name) || row.integrationId,
          registryId: row.integrationId,
          endpoint: row.endpoint,
          authRef: row.authRef,
          baseUrl: row.baseUrl,
          status: "untested",
          lastTested: "",
          lastResponse: "",
          entityType: row.entityTypes || `${row.integrationId}.records`,
          sourceId: row.integrationId,
          sourceStorage: "workspace-source-records",
          resolverTemplateId: row.resolverTemplateId,
        },
        linkage: { registryField: "registryId", value: row.integrationId },
        refreshHint: "After the API test passes, Refresh sources to populate growthub.source-records.json.",
      }
    : { create: false };

  return {
    kind: "growthub-api-registration-plan-v1",
    integrationId: row.integrationId,
    errors,
    valid: errors.length === 0,
    dataSource,
    config: {
      mode: apiObject ? "row.add" : "object.create",
      objectId: apiObject ? clean(apiObject.id) : null,
      objectLabel: apiObject ? (apiObject.label || apiObject.source) : "API Registry",
      columns: API_REGISTRY_COLUMNS,
      row,
    },
    resolver,
    env: {
      authRef: row.authRef,
      required: Boolean(row.authRef),
      configured: authConfigured,
      candidateNames: envCandidateNames(row.authRef),
      hint: row.authRef
        ? (authConfigured
            ? `Resolves from an env key for "${row.authRef}".`
            : `No env value resolves for "${row.authRef}" yet. Add it in Settings → APIs & Webhooks (.env.local).`)
        : "No auth — public endpoint.",
    },
    testPlan: {
      method: row.method,
      url: testUrl,
      authRef: row.authRef || null,
      route: "/api/workspace/test-api-record",
    },
    rollback: {
      config: apiObject ? `Remove the "${row.integrationId}" row from the API Registry object` : "Delete the created API Registry object",
      resolver: needsResolver ? `Delete ${resolverPath(row.integrationId)}` : null,
    },
  };
}

/** Per-plan readiness for the wizard's final step (no fake "ready"). */
function summarizePlanReadiness(plan, { resolverPresent = false, row = null } = {}) {
  const r = row || plan?.config?.row || {};
  const steps = [
    { id: "registered", label: "API Registry row", status: plan?.config?.row?.integrationId ? "complete" : "pending" },
    { id: "auth", label: "Auth key resolves", status: !plan?.env?.required ? "optional" : (plan?.env?.configured ? "complete" : "pending") },
  ];
  if (plan?.resolver?.required) {
    steps.push({ id: "resolver", label: "Resolver file written", status: resolverPresent ? "complete" : "pending" });
  }
  steps.push({ id: "tested", label: "API test passed", status: SUCCESS_STATUSES.has(clean(r.status)) ? "complete" : "pending" });
  const blocking = steps.filter((s) => s.status === "pending");
  return { steps, complete: blocking.length === 0, nextStepId: blocking.length ? blocking[0].id : null };
}

export {
  API_REGISTRY_COLUMNS,
  normalizeApiRegistryInput,
  resolverRequired,
  resolverFileName,
  resolverPath,
  buildResolverSource,
  buildApiRegistrationPlan,
  summarizePlanReadiness,
};

/**
 * Governed Marketplace Catalog — the real 2026 provider universe as governed
 * capability descriptors (GOVERNED_CAPABILITY_BINDING_V1).
 *
 * This module is the provider-agnostic *capability* layer of the marketplace. It
 * carries NO runtime, NO fetch, NO config writes — it is pure descriptor data,
 * identical in shape to the Upstash descriptors that the QStash serverless
 * scheduler proved out (see `workspace-add-ons.js`). Every entry here is
 * transcribed from the provider's OFFICIAL, live API documentation (base URL,
 * auth scheme, safe read-only probe path, concrete env keys) so the marketplace
 * scales to the real provider universe without inventing shapes.
 *
 * The QStash spike forged the atoms; this catalog clones that geometry outward:
 *
 *   providerId · integrationId · authRef · executionLane · requiredEnv ·
 *   probe · connectorKind · capabilities · category
 *
 * plus TWO additions the closed-loop product model requires (and QStash proved
 * implicitly):
 *
 *   auth        — the auth-scheme atom. QStash's account lane was hardcoded to
 *                 HTTP-Basic email:key; the real universe is bearer / basic /
 *                 custom-header. `buildProviderAuthHeaders` (capability-binding.js)
 *                 turns this atom into a request's auth headers, secret-side only.
 *   nodeSurface — how the capability appears on the workflow canvas so an agent /
 *                 graph can USE it. `trigger` (schedulers, mirrors QStash) or
 *                 `api-registry-call` (send / query / deploy / inference). This is
 *                 the "correlated to a governed API Registry data object" rule:
 *                 the node's registryId points at the installed API Registry row.
 *
 * SECRET RULE (unchanged from Upstash): descriptors carry only env KEY NAMES.
 * No token, key, or signing secret is ever a value in this file, in a row, in a
 * receipt, or in a browser payload.
 */

